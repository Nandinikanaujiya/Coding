#include<iostream>
using namespace std;

int main() {
    int a = 4, b = 5;
    cout<<"a+b is"<<a+b<<endl;
    cout<<"a-b"<<a-b<<endl;
    cout<<"a*b"<<a*b<<endl;
    cout<<"a\b"<<a/b<<endl;
    cout<<"a%b"<<a%b<<endl;
    cout<<"a++"<<a++<<endl;
    cout<<"a--"<<a--<<endl;
    cout<<"++a"<<++a<<endl;
    cout<<"--a"<<--a<<endl;


    //comparison opertors
    cout<<"the value of a== b is"<<(a==b)<<endl;
    cout<<"the value of a!= b is"<<(a!=b)<<endl;
    cout<<"the value of a > b is"<<(a >b)<<endl;
    cout<<"the value of a < b is"<<(a <b)<<endl;
    cout<<"the value of a<= b is"<<(a<=b)<<endl;
    cout<<"the value of a>= b is"<<(a>=b)<<endl;
    return 0;

}